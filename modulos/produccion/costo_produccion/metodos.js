//Busca, filtra y muestra la orden produccion
const getOrdenProduccion = () => {
  $.ajax({
    //Solicitamos los datos a listaOrdenProduccionCosto
    method: "POST",
    url: "/sys8DD/others/complements_php/listasMovimientos/listaOrdenProduccionCosto.php",
    data: {
      secc_descripcion: $("#secc_descripcion").val(),
      suc_codigo: $("#suc_codigo").val(),
    },
  }) //Individualizamos los datos del array y lo separamos por lista
    .done(function (lista) {
      let fila = "";
      $.each(lista, function (i, objeto) {
        if (objeto.dato1 == "NSE") {
          fila += "<li class='list-group-item'>" + objeto.dato2 + "</li>";
        } else {
          fila +=
            "<li class='list-group-item' onclick='seleccionOrdenProduccion(" +
            JSON.stringify(objeto) +
            ")'>" +
            objeto.orden +
            "</li>";
        }
      });
      //cargamos la lista
      $("#ulOrdenProduccion").html(fila);
      //hacemos visible la lista
      $("#listaOrdenProduccion").attr(
        "style",
        "display: block; position:absolute; z-index: 3000; width:100%;"
      );
    })
    .fail(function (a, b, c) {
      swal("ERROR", c, "error");
    });
};

